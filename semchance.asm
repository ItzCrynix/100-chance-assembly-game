move:
	; Verifies if a key was pressed
	inchar r1
	loadn r2, #255
	cmp r1, r2
	jeq move
	
	; Verifies whether the pressed key was WASD
	loadn r2, #'w'
	cmp r1, r2
	jeq move_up

	loadn r2, #'s'
	cmp r1, r2
	jeq move_down

	loadn r2, #'a'
	cmp r1, r2
	jeq move_left

	loadn r2, #'d'
	cmp r1, r2
	jeq move_right

	jmp move

	move_up:
		load r0, playerPos
	
		; Checking if player is at the top (pos < 40)
		loadn r2, #40
		cmp r0, r2
		jle move

		; New position that the player would have, needs to be checked
		call clearPlayer
        mov r3, r0 ; temporarily saving player position
		sub r0, r0, r2
		call move_checkCollision

		; New position gets saved.
		store playerPos, r0

		rts

	move_down:
		load r0, playerPos
	
		; Checking if player is at the bottom (pos > 1159)
		loadn r2, #1159
		cmp r0, r2
		jgr move

		; New position that the player would have, needs to be checked
		loadn r2, #40
		call clearPlayer
        mov r3, r0 ; temporarily saving player position
		add r0, r0, r2
		call move_checkCollision

		; New position gets saved.
		store playerPos, r0

		rts

	move_left:
		load r0, playerPos
	
		; Checking if player is at the left border
		loadn r2, #40
		mod r2, r0, r2
		loadn r3, #0
		cmp r2, r3
		jeq move

		; New position that the player would have, needs to be checked
		call clearPlayer
        mov r3, r0 ; temporarily saving player position
		dec r0
		call move_checkCollision

		; New position gets saved.
		store playerPos, r0

		rts

	move_right:
		load r0, playerPos
	
		; Checking if player is at the right border
		loadn r2, #40
		mod r2, r0, r2
		loadn r3, #39
		cmp r2, r3
		jeq move

		; New position that the player would have, needs to be checked
		call clearPlayer
        mov r3, r0 ; temporarily saving player position
		inc r0
		call move_checkCollision

		; New position gets saved.
		store playerPos, r0

		rts

    move_checkCollision:
        push r1
        push r2

        ; getting char at players next position
        load r1, mapListIndex
        loadn r2, #mapList
        add r1, r1, r2
        loadi r1, r1

        add r1, r1, r0
        loadi r1, r1

        ; getting the char's value
        loadn r2, #128
        mod r1, r1, r2
        
        ; checking if it's greater than 1 (walls go from 1 to 12)
        loadn r2, #1
        cmp r1, r2
        ceg move_checkCollision_Condition
        
        pop r2
        pop r1
        rts

        move_checkCollision_Condition:
            ; checking if it's less than 12
            loadn r2, #14 
            cmp r1, r2
            jel move_checkCollision_Wall

            loadn r2, #36
            cmp r1, r2
            jeq move_checkCollision_Money

            loadn r2, #25
            cmp r1, r2
            jeq move_checkCollision_ladder

            rts
    
        move_checkCollision_Wall:
            mov r0, r3
            rts

        move_checkCollision_ladder:
            ; takes the pointer of the current screen and move to the next
            load r2, mapListIndex
            loadn r3, #2
            cmp r2, r3
            jeq move_checkCollision_ladder_exit

            inc r2
            store mapListIndex, r2

            move_checkCollision_ladder_exit:
                rts

        move_checkCollision_Money:
            load r2, money
            inc r2
            store money, r2
            rts
            

