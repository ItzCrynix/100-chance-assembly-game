main:
    call printCurrentScreen

	main_game_loop:
		; checking if the game reset
		load r1, gameReset
		loadn r2, #0
		cmp r1, r2
		jne main_reset

        call printStepCount
        call printScore
        call printMoney
		call printPlayer

		; calculation functions
		call move
		call checkSteps
		
		jmp main_game_loop

    main_reset:
        ; saves the last map index
        load r2, mapListIndex
        push r2
        
        ; sets the index to the death screen
        loadn r2, #0
        store mapListIndex, r2
        
        call printCurrentScreen

        main_reset_loop:
            ; wait for '0' input
            inchar r1
            loadn r2, #255
            cmp r1, r2
            jeq main_reset_loop
            
            loadn r2, #'0'
            cmp r1, r2
            jne main_reset_loop

            ; sets the reset flag to 0 (false)
            loadn r2, #0
            store gameReset, r2

            pop r2
            store mapListIndex, r2

            call main_reset_player

            jmp main

        main_reset_player:
            ; resets the current steps of the player
		    load r2, maxSteps
		    store currentSteps, r2

		    ; moves the player back to the spawn point
		    load r2, mapListIndex
            loadn r3, #mapSpawn
            add r3, r3, r2
            loadi r3, r3
		    store playerPos, r3
            
            rts
		

