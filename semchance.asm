	loadn r2, #'\0' ; stop condition
	loadn r4, #0

	printString_Loop:
        ; passes through all the character address and loads to r3
		add r3, r1, r4
		loadi r3, r3
		cmp r3, r2
		jeq printString_Leave
		outchar r3, r0
		inc r0
		inc r4
		jmp printString_Loop

	printString_Leave:
		rts

; prints any given number
; r0 = position, r1 = number
; caution: it only prints numbers up to 16 bits or 65536
printNumber:
	push r2
	push r3
	push r4
	push r5

	mov r4, r1
	loadn r5, #0
	
	getDigits_Loop:
		; gets the rightmost digit of the number, pushes it into the stack
		loadn r2, #10
		mod r3, r4, r2
		push r3
		; divides the whole number by 10 to remove the last digit (which was added to the stack)
		div r4, r4, r2
		
		; r5 stores the number of iterations that will be done while printing
		inc r5
		
		; see if the number is equal to 0, leaves if so
		loadn r2, #0
		cmp r4, r2
		jeq printNumber_Loop
		jmp getDigits_Loop

	printNumber_Loop:
		; gets leftmost digit of the number and then prints it
		pop r3

		loadn r4, #48 ; 48 = ascii value of '0'
		add r3, r3, r4

		outchar r3, r0
		inc r0

		dec r5
		cmp r5, r2
		jeq printNumber_Leave
		jmp printNumber_Loop

	printNumber_Leave:
		pop r5
		pop r4
		pop r3
		pop r2
		rts

